getal1 = int(input('Geef getal1: '))
getal2 = int(input('Geef getal2: '))
som = getal1 + getal2
verschil = getal1 - getal2
product = getal1*getal2
quotient = getal1/getal2
print('De som van', getal1, 'en', getal2, 'is', som)
print('Het verschil van', getal1, 'en', getal2, 'is', verschil)
print('Het product van', getal1, 'en', getal2, 'is', product)
print('Het quotient', getal1, 'en', getal2, 'is', quotient)


